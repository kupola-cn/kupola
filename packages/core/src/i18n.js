const _localeSignal = signal(detectLocale());

const _localeChangeListeners = new Set();

function detectLocale() {
  if (typeof window === 'undefined') {
    return 'en-US';
  }

  const urlParams = new URLSearchParams(window.location.search);
  if (urlParams.has('lang')) {
    return urlParams.get('lang');
  }

  if (typeof localStorage !== 'undefined' && localStorage.getItem('kupola-locale')) {
    return localStorage.getItem('kupola-locale');
  }

  if (typeof navigator !== 'undefined') {
    return navigator.language || navigator.userLanguage || 'en-US';
  }

  return 'en-US';
}

export function setLocale(locale) {
  const oldLocale = _localeSignal.value;
  _localeSignal.value = locale;

  if (typeof localStorage !== 'undefined') {
    localStorage.setItem('kupola-locale', locale);
  }

  for (const listener of _localeChangeListeners) {
    listener(locale, oldLocale);
  }
}

export function getLocale() {
  return _localeSignal.value;
}

export function t(key, params = {}) {
  const locale = _localeSignal.value;
  const messages = _messages[locale] || _messages['en-US'];
  let text = messages[key] || key;

  for (const [k, v] of Object.entries(params)) {
    text = text.replace(`{${k}}`, v);
  }

  return text;
}

export function addMessages(locale, messages) {
  if (!_messages[locale]) {
    _messages[locale] = {};
  }
  Object.assign(_messages[locale], messages);
}

export function getMessages(locale) {
  return _messages[locale] || {};
}

export function getSupportedLocales() {
  return Object.keys(_messages);
}

export function formatDate(date, options = {}) {
  const locale = getLocale();
  return new Intl.DateTimeFormat(locale, options).format(date);
}

export function formatNumber(num, options = {}) {
  const locale = getLocale();
  return new Intl.NumberFormat(locale, options).format(num);
}

export function formatCurrency(num, currency = 'USD', options = {}) {
  const locale = getLocale();
  return new Intl.NumberFormat(locale, { ...options, style: 'currency', currency }).format(num);
}

export function formatRelativeTime(value, unit, options = {}) {
  const locale = getLocale();
  return new Intl.RelativeTimeFormat(locale, options).format(value, unit);
}

export function isRTL(locale) {
  const rtlLocales = ['ar', 'he', 'fa', 'ur', 'ps', 'sd'];
  return rtlLocales.includes((locale || getLocale()).split('-')[0]);
}

export function getDirection(locale) {
  return isRTL(locale) ? 'rtl' : 'ltr';
}

export function onLocaleChange(callback) {
  _localeChangeListeners.add(callback);
  return () => {
    _localeChangeListeners.delete(callback);
  };
}

export { _localeSignal as localeSignal };